def sort_array_asc(numbers)
  numbers.sort { |a, b| a <=> b }
end

def sort_array_desc(numbers)
  numbers.sort { |a, b|
    if a == b
      0
    elsif a > b
      -1
    elsif a < b
      1
    end
  }
end

